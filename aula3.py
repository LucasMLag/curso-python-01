# Aula 3: Linguagem dinamica, strings

"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Sou uma string definida em aspas simples')

# Aspas duplas
print("Sou uma string definida em aspas duplas")

# Escape - Usado para exibir caracteres especiais sem que tenham função de código
print("Isso é um \"escape\"")

# É possivel por " em uma string sem escape se ela for aberta e fechada por '
print('E deus disse: "Que haja luz"')

# É possivel por ' em uma string sem escape se ela for aberta e fechada por "
print("Joana D'arc")

# r - Expressão regular, veremos sobre em outra aula
print(r"Isso aqui é \"confuso\"")